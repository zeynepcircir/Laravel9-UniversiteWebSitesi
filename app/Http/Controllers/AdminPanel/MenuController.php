    {
        //
        $data= new Menu();
        $data->parent_id = $request->parent_id=0;
        $data->title = $request->title;
        $data->keywords = $request->keywords;
        $data->description = $request->description;
        $data->status= $request->status;
        $data->save();
         return redirect('admin/menu');   
    }

